# 유클리드 호제법으로 최대공약수를 구하는 과정입니다.
a, b = 48, 18  # 예시 숫자입니다. 실제로는 입력으로 받습니다.
while b != 0:
    temp = b       # b값을 임시로 저장합니다.
    b = a % b      # a를 b로 나눈 나머지를 b에 저장합니다.
    a = temp       # 이전 b 값을 a에 저장합니다.
# while문이 끝나면 a에 최대공약수가 저장되어 있습니다.

# 실제 문제에서는 위를 함수 없이 반복문으로 처리하고, 
# 테스트 케이스를 입력받아 결과를 출력합니다.

# 테스트 케이스 수 입력받기
T = int(input()) 

for _ in range(T):
    # A, B, X 값을 한 줄로 입력받아 정수로 나눠서 저장
    A, B, X = map(int, input().split())

    # 최대공약수를 구하기 위한 변수 초기화
    a = A
    b = B

    # 최대공약수 구하는 반복문 시작
    while b != 0:
        temp = b
        b = a % b
        a = temp
    # 최대공약수는 a에 저장되어 있음

    # X를 최대공약수로 나눈 몫이 정답
    print(X // a)